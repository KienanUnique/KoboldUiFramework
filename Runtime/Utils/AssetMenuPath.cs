namespace KoboldUi.Utils
{
    /// <summary>
    /// Provides common paths for Unity asset menu entries used by the framework.
    /// </summary>
    public static class AssetMenuPath
    {
        /// <summary>
        /// Root menu path for Kobold UI assets.
        /// </summary>
        public const string ROOT = "Kobold UI/";

        /// <summary>
        /// Menu path for animation parameter assets.
        /// </summary>
        public const string ANIMATION_PARAMETERS = ROOT + "Animation Parameters/";

        /// <summary>
        /// Menu path for installer assets.
        /// </summary>
        public const string INSTALLERS = ROOT + "Installers/";
    }
}
